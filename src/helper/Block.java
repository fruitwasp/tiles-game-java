package helper;

import javax.swing.JPanel;

@SuppressWarnings("serial")
public class Block extends JPanel {
	
	private int x, y;
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
}


